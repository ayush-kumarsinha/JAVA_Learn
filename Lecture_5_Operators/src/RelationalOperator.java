public class RelationalOperator {
    public static void main(String[] args) {

        int p = 34;
        int q = 67;

        System.out.println(p==q);  //false
        System.out.println(p!=q);  //true
        System.out.println(p>q);  //false
        System.out.println(p<q);  //true
        System.out.println(p>=q);  //false
        System.out.println(p<=q);  //true
    }
}
